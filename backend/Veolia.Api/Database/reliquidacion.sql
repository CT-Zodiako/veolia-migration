-- Vista comparativo de costos
CREATE OR REPLACE VIEW VREL_COMPARACOSTOS AS
SELECT R.RELQID AS COD_RELIQ,
       R.APSA_ID,
       R.RELQDESDE,
       R.RELQHASTA,
       NVL(A.COSTO, 0) AS COSTO_APS,
       NVL(E.COSTO, 0) AS COSTO_EMPRESA,
       (NVL(A.COSTO, 0) - NVL(E.COSTO, 0)) AS DIF_COSTO
  FROM RELQRELIQUIDA R
  LEFT JOIN RELI_INFOAPSEMPRDIVI A ON A.RELI_ID = R.RELQID
  LEFT JOIN RELI_INFOEMPRDIVI E ON E.RELI_ID = R.RELQID;

-- Vista comparativo tarifa de cobro
CREATE OR REPLACE VIEW VREL_COMPARATARIFACOBRO AS
SELECT R.RELQID AS RELI,
       R.APSA_ID,
       NVL(A.TARIFA, 0) AS TARIFA_APS,
       NVL(E.TARIFA, 0) AS TARIFA_EMPRESA,
       (NVL(A.TARIFA, 0) - NVL(E.TARIFA, 0)) AS DIF_TARIFA
  FROM RELQRELIQUIDA R
  LEFT JOIN RELI_INFOAPSEMPRDIVI A ON A.RELI_ID = R.RELQID
  LEFT JOIN RELI_INFOEMPRDIVI E ON E.RELI_ID = R.RELQID;

