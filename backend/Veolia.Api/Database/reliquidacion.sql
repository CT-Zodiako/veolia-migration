  RELQHASTA VARCHAR2(6) NOT NULL,
  RELQESTADO VARCHAR2(20) DEFAULT 'CREADA' NOT NULL,
  USUA_USUA NUMBER,
  RELQFECHA DATE DEFAULT SYSDATE,
